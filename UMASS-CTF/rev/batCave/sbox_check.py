def verify_sbox():
    """Verify SBOX data completeness and integrity"""
    
    print("=" * 70)
    print("SBOX Verification Report")
    print("=" * 70)
    
    # Basic statistics
    total_entries = len(SBOX_DATA)
    print(f"\n[*] Total entries: {total_entries}")
    
    if total_entries == 256:
        print("    ✓ Correct length (256 bytes)")
    else:
        print(f"    ✗ WRONG LENGTH! Expected 256, got {total_entries}")
    
    # Check for duplicates
    seen = {}
    duplicates = {}
    for i, val in enumerate(SBOX_DATA):
        if val in seen:
            if val not in duplicates:
                duplicates[val] = [seen[val]]
            duplicates[val].append(i)
        else:
            seen[val] = i
    
    if duplicates:
        print(f"\n[!] Found {len(duplicates)} DUPLICATE VALUES:")
        for val, positions in sorted(duplicates.items()):
            print(f"    Value 0x{val:02x}: appears at indices {positions}")
    else:
        print(f"\n[✓] No duplicates found - all values appear exactly once")
    
    # Check for missing values
    all_bytes = set(range(256))
    present_bytes = set(SBOX_DATA)
    missing = sorted(all_bytes - present_bytes)
    
    if missing:
        print(f"\n[!] Missing {len(missing)} VALUES from 0-255:")
        # Display missing values in rows of 16
        for i in range(0, len(missing), 16):
            chunk = missing[i:i+16]
            hex_chunk = ' '.join(f'0x{b:02x}' for b in chunk)
            print(f"    {hex_chunk}")
    else:
        print(f"\n[✓] All 256 values (0x00-0xFF) are present")
    
    # Summary
    print("\n" + "=" * 70)
    if total_entries == 256 and not duplicates and not missing:
        print("✓ SBOX is COMPLETE and VALID (perfect permutation)")
    else:
        print("✗ SBOX has ISSUES - see above")
    print("=" * 70)

if __name__ == "__main__":
    verify_sbox()
